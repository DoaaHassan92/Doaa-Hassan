package products.create;

public class ProductInfo {
    private String name;
    private String type;
    private int price;
    private String upc;
    private String description;
    private String model;

    public ProductInfo(String name, String type, int price, String upc, String description, String model) {
        this.name = name;
        this.type = type;
        this.price = price;
        this.upc = upc;
        this.description = description;
        this.model = model;
    }



}
